public class Loopsprblm1 {
    public static void main(String[] args) {
        //for loop
        for (int i=0;i<4;i++){
            System.out.print(i+"->");
        }
        System.out.println("null");
    // while loop
        int j=0;
        while (j<10){
            System.out.print(j+"->");
            j++;
        }
        System.out.println("null");

    // do while
        int k =0;
        do {
            System.out.print(k+"->");
            k++;
        } while (k<10);
        System.out.println("null");
    }
}
